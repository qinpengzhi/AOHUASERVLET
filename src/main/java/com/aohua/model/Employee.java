package com.aohua.model;

public class Employee {
	private Integer EmpID;
	private String EmpCode;//员工编号
	private String EmpName;//姓名
	private Integer Gender;//性别
	private String EduBackGround;//学历
	private String Birthday;//生日
	private String Duty;//职务
	private Integer DeptID;//部门id
	private String InCompanyDate;//进入公司日期
	private String ContactMode;//联系方式
	private String LeaveDate;//离职日期
	private String IDCard;//身份证号
	public Integer getEmpID() {
		return EmpID;
	}
	public void setEmpID(Integer empID) {
		EmpID = empID;
	}
	public String getEmpCode() {
		return EmpCode;
	}
	public void setEmpCode(String empCode) {
		EmpCode = empCode;
	}
	public String getEmpName() {
		return EmpName;
	}
	public void setEmpName(String empName) {
		EmpName = empName;
	}
	public Integer getGender() {
		return Gender;
	}
	public void setGender(Integer gender) {
		Gender = gender;
	}
	public String getEduBackGround() {
		return EduBackGround;
	}
	public void setEduBackGround(String eduBackGround) {
		EduBackGround = eduBackGround;
	}
	public String getBirthday() {
		return Birthday;
	}
	public void setBirthday(String birthday) {
		Birthday = birthday;
	}
	public String getDuty() {
		return Duty;
	}
	public void setDuty(String duty) {
		Duty = duty;
	}
	public Integer getDeptID() {
		return DeptID;
	}
	public void setDeptID(Integer deptID) {
		DeptID = deptID;
	}
	public String getInCompanyDate() {
		return InCompanyDate;
	}
	public void setInCompanyDate(String inCompanyDate) {
		InCompanyDate = inCompanyDate;
	}
	public String getContactMode() {
		return ContactMode;
	}
	public void setContactMode(String contactMode) {
		ContactMode = contactMode;
	}
	public String getLeaveDate() {
		return LeaveDate;
	}
	public void setLeaveDate(String leaveDate) {
		LeaveDate = leaveDate;
	}
	public String getIDCard() {
		return IDCard;
	}
	public void setIDCard(String iDCard) {
		IDCard = iDCard;
	}
	
}
